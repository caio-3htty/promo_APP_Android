package com.prumo.core.i18n

import androidx.compose.runtime.Composable
import androidx.compose.runtime.staticCompositionLocalOf
import com.prumo.core.model.AppLanguage

class I18nCatalog(private val language: AppLanguage) {
    fun t(key: String, vararg args: Pair<String, Any?>): String {
        val value = strings[language]?.get(key)
            ?: strings[AppLanguage.PT_BR]?.get(key)
            ?: key
        if (args.isEmpty()) return value

        var rendered = value
        args.forEach { (name, raw) ->
            rendered = rendered.replace("%{$name}", raw?.toString().orEmpty())
        }
        return rendered
    }
}

val LocalI18n = staticCompositionLocalOf { I18nCatalog(AppLanguage.PT_BR) }

@Composable
fun t(key: String, vararg args: Pair<String, Any?>): String = LocalI18n.current.t(key, *args)

@Composable
fun statusLabel(rawStatus: String): String {
    val key = "status.$rawStatus"
    val translated = LocalI18n.current.t(key)
    return if (translated == key) rawStatus else translated
}

